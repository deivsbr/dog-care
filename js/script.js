function openNav(){
    document.getElementById("minhaBarra").style.width = "250px";
    document.getElementById("menu").style.marginLeft = "250px";
}

function closeNav(){
    document.getElementById("minhaBarra").style.width = "0";
    document.getElementById("menu").style.width = "0";
    document.getElementById("menu").style.marginLeft = "10px";
}